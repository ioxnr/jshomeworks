function addition(a, b) {
    return a + b;
}

function subtraction(a, b) {
    return a - b;
}

function multiplication(a, b) {
    return a * b;
}

function division(a, b) {
    return a / b;
}

console.log(addition(3, 6));
console.log(subtraction(3, 6));
console.log(multiplication(3, 6));
console.log(division(3, 6));
